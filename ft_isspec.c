#include "ft_printf.h"

int		ft_findspec(char c)
{
	if (c == 'd' || c == 'i' || c == 'u' || c == 'o' || c == 'x' || c == 'X')
		return (1);
	if (c == 'f' || c == 'F' || c == 'e' || c == 'E' || c == 'g' || c == 'G')
		return (1);
	if (c == 'a' || c == 'A' || c == 'c' || c == 's' || c == 'p' || c == '%')
		return (1);
	if (c == 'S' || c == 'n')
		return (1);
	return (0);
}
