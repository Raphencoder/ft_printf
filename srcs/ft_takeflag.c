#include "ft_printf.h"

char	*ft_takeflag(char *str)
{
	int		i;
	char	*res;

	i = 0;
	while (str[i] && !ft_isspec(str[i]))
		i++;
	if (str[i] == 'U' || str[i] == 'D' || str[i] == 'O')
	{
		res = "l";
		return (res);
	}
	if (i == 0)
		return (NULL);
	res = ft_strndup(str, i);
	return (res);
}
